    public void tampilDataBooking(){
        try {
            modelTabelBooking.getDataVector().removeAllElements();
            modelTabelBooking.fireTableDataChanged();
        
            PreparedStatement query = myObject.Konek.prepareStatement("SELECT * FROM tbl_penyewaan");
            ResultSet data = query.executeQuery();
            while (data.next()) {
            Object[] baris = new Object[5];
            baris[0] = data.getString("ID_Penyewaan");
            baris[1] = data.getString("Nama_Penyewaan");
            baris[2] = data.getString("Tanggal_Sewa");
            baris[3] = data.getString("Tanggal_Kembali");
            baris[4] = data.getString("Total_Biaya");
            modelTabelBooking.addRow(baris);
            
            }
            query.close();
            data.close();
            } catch (Exception e){
            }
    }
